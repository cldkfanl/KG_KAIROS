import turtle as t

t.pensize(5)
t.pencolor('blue')
t.speed(5)
t.penup()
t.goto(-100, 0)
t.pendown()
t.circle(50)

t.penup()
t.pencolor('yellow')
t.goto(-50, -50)
t.pendown()
t.circle(50)

t.penup()
t.pencolor('black')
t.goto(0, 0)
t.pendown()
t.circle(50)

t.penup()
t.pencolor('green')
t.goto(50, -50)
t.pendown()
t.circle(50)

t.penup()
t.pencolor('red')
t.goto(100, 0)
t.pendown()
t.circle(50)
