import turtle as t

t.shape('turtle')
t.pensize(5)
t.speed(5)
t.fillcolor('red')
t.begin_fill()
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.end_fill()
"""draw a blue triangle"""
t.fillcolor('blue')
t.begin_fill()
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
t.end_fill()

my_t = t.Turtle()
my_t.speed(1)
my_t.goto(200,0)
my_t.forward(100)
my_t.left(90)
my_t.forward(100)
my_t.left(90)
my_t.forward(100)
my_t.left(90)
my_t.forward(100)
my_t.write('안녕하세요', font=('Arial', 12, 'normal'))
